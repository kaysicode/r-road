# Operators:
# < : Less Than
# <= : Less Than Equal To
# > : Greater Than
# >= : Greater Than Equal To
# == : Equal To
# != Not Equal To

# The result will be in logical values (TRUE/FALSE)

x <- 20
y <- 10
i <- 10

# The value of Z is logical value
z <- x < 10
print(z)
z <- y <= i
print(z)
z <- y >= i
print(z)
z <- x > i
print(z)
z <- y != i
print(z)
