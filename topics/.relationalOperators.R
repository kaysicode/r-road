# Operators:
# < : Less Than
# <= : Less Than Equal To
# > : Greater Than
# >= : Greater Than Equal To
# == : Equal To
# != Not Equal To

# The result will be in logical values (TRUE/FALSE)

x <- 20
y <- 10
i <- 10

# The value of Z is logical value
z <- x < 10
print(z)
z <- y <= i
print(z)
z <- y >= i
print(z)
z <- x > i
print(z)
z <- y != i
print(z)

# We can also compare or relate the two character
char1 <- "Hello"
char2 <- "Program"

result <- char1 == char2
print(result)
result <- char2 > char1
print(result)

